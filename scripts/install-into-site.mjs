#!/usr/bin/env node
// Install the embedded admin panel into a host site.
//
// Run this from a host site that has added admin_panel as a git submodule
// (default location: vendor/admin-panel). It mirrors every admin route from the
// submodule into the host's src/app as a thin re-export wrapper, so Next.js
// (whose routing is file-system based) picks them up while the real code stays
// in the submodule and updates with `git submodule update --remote`.
//
// Usage:
//   node vendor/admin-panel/scripts/install-into-site.mjs [--submodule vendor/admin-panel] [--site .] [--force]
//
// After running, follow the printed checklist (tsconfig path, middleware, deps).

import fs from 'node:fs';
import path from 'node:path';

const args = process.argv.slice(2);
const getArg = (name, def) => {
  const i = args.indexOf(`--${name}`);
  return i >= 0 && args[i + 1] ? args[i + 1] : def;
};
const force = args.includes('--force');

const siteRoot = path.resolve(getArg('site', process.cwd()));
const submoduleRel = getArg('submodule', 'vendor/admin-panel');
const submoduleRoot = path.resolve(siteRoot, submoduleRel);
const submoduleSrcApp = path.join(submoduleRoot, 'src', 'app');
const hostSrcApp = path.join(siteRoot, 'src', 'app');

if (!fs.existsSync(submoduleSrcApp)) {
  console.error(`✗ Could not find ${submoduleSrcApp}\n  Add the submodule first:\n  git submodule add https://github.com/RHC-Solutions/admin_panel.git ${submoduleRel}`);
  process.exit(1);
}

// Which subtrees of the submodule's app/ are the admin panel's routes.
const ROUTE_ROOTS = ['admin', 'api/admin', 'api/cms', 'api/auth'];

// Files Next treats as routable; how each should be re-exported.
const DEFAULT_AND_NAMED = new Set([
  'page.tsx', 'layout.tsx', 'template.tsx', 'loading.tsx',
  'error.tsx', 'not-found.tsx', 'default.tsx',
]);
const NAMED_ONLY = new Set(['route.ts', 'route.tsx']);

const walk = (dir, acc = []) => {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, acc);
    else acc.push(full);
  }
  return acc;
};

let created = 0, skipped = 0, clientWarned = 0;
const summary = [];

for (const root of ROUTE_ROOTS) {
  const startDir = path.join(submoduleSrcApp, root);
  if (!fs.existsSync(startDir)) continue;

  for (const file of walk(startDir)) {
    const base = path.basename(file);
    const isDefault = DEFAULT_AND_NAMED.has(base);
    const isNamed = NAMED_ONLY.has(base);
    if (!isDefault && !isNamed) continue; // skip helpers, components, css, etc.

    const relFromApp = path.relative(submoduleSrcApp, file); // e.g. admin/dashboard/page.tsx
    const importSpec = '@adminpanel/app/' + relFromApp.replace(/\.(tsx?|jsx?)$/, '');
    const target = path.join(hostSrcApp, relFromApp);

    if (fs.existsSync(target) && !force) { skipped++; continue; }

    // Error/loading/template/global components must carry 'use client' in the
    // file itself; a pure re-export can't. Flag those so the host can copy
    // them instead of wrapping (rare in this admin panel).
    const srcText = fs.readFileSync(file, 'utf8').slice(0, 40);
    const isClientBoundaryFile = /error\.tsx$|global-error\.tsx$/.test(base) && /['"]use client['"]/.test(srcText);

    let body;
    if (isNamed) {
      body = `// AUTO-GENERATED by admin-panel/scripts/install-into-site.mjs — do not edit.\nexport * from '${importSpec}';\n`;
    } else {
      body = `// AUTO-GENERATED by admin-panel/scripts/install-into-site.mjs — do not edit.\nexport { default } from '${importSpec}';\nexport * from '${importSpec}';\n`;
      if (isClientBoundaryFile) { body = `'use client';\n` + body; clientWarned++; }
    }

    fs.mkdirSync(path.dirname(target), { recursive: true });
    fs.writeFileSync(target, body);
    created++;
    summary.push(path.relative(siteRoot, target));
  }
}

console.log(`\n✓ admin panel wrappers: ${created} created, ${skipped} skipped${force ? '' : ' (existing; use --force to overwrite)'}.`);
if (created <= 40) summary.forEach((s) => console.log('  + ' + s));

console.log(`\nNext steps (one-time per site):

1) tsconfig.json — add the path alias so wrappers resolve into the submodule:
     "compilerOptions": { "paths": {
        "@/*": ["./src/*"],
        "@adminpanel/*": ["./${submoduleRel}/src/*"]
     }}

2) middleware.ts — compose the admin auth gate (keep your site's own logic):
     import { adminAuthGate, ADMIN_MATCHER } from '@adminpanel/admin-middleware';
     export async function middleware(req) {
       const gate = await adminAuthGate(req);
       if (gate) return gate;
       return NextResponse.next();
     }
     export const config = { matcher: [ ...ADMIN_MATCHER ] };

3) Dependencies — ensure the host has the admin panel's deps:
     node ${submoduleRel}/scripts/install-into-site.mjs --print-deps   (lists them)
   then \`npm i <those> --legacy-peer-deps\`.

4) Env (.env.local): NEXTAUTH_SECRET, NEXTAUTH_URL, NEXT_PUBLIC_SITE_URL.

5) Data: the admin reads/writes ./cms-data in the host site (its own theme,
   pages, users, secrets). Nothing to share — each site keeps its own.
`);

if (args.includes('--print-deps')) {
  const pkg = JSON.parse(fs.readFileSync(path.join(submoduleRoot, 'package.json'), 'utf8'));
  console.log('Runtime deps to install in the host site:\n');
  console.log(Object.entries(pkg.dependencies || {}).map(([k, v]) => `  ${k}@${v}`).join('\n'));
}
